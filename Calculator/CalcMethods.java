package Calculator;

public class CalcMethods {
    public int Add(int a,int b){
        return a+b;
    }
    public int Sub(int a,int b){
        return a-b;
    }
    public int Mult(int a,int b){
        return a*b;
    }
    public int Div(int a,int b){
        return a/b;
    }

    public int Mod(int a,int b){
        return a%b;
    }
}
